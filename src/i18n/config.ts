for (const [code, value] of Object.entries(dict)) {
  resources[code] = { translation: value };
}

// Merge `app` namespace into each language. Languages without explicit
// `app` translations inherit the English app dictionary so the app shell
// never mixes languages.
const merged: Record<string, any> = {};
for (const code of Object.keys(resources)) {
  const appPart = APP_TRANSLATIONS[code] ?? APP_FALLBACK;
  const jsonPart = JSON_BUNDLES[code] ?? JSON_BUNDLES.en;
  merged[code] = {
    translation: { ...resources[code].translation, ...appPart, ...jsonPart },
  };
}

i18n
  .use(LanguageDetector)
  .use(initReactI18next)
  .init({
    resources: merged,
    fallbackLng: "en",
    supportedLngs: LANGUAGES.map((l) => l.code),
    interpolation: { escapeValue: false },
    detection: { order: ["localStorage", "navigator"], caches: ["localStorage"] },
    returnEmptyString: false,
    returnNull: false,
  });

// Update <html> lang & dir on language change
const applyDir = (lng: string) => {
  if (typeof document === "undefined") return;
  document.documentElement.lang = lng;
  document.documentElement.dir = ["ar", "he"].includes(lng) ? "rtl" : "ltr";
};
applyDir(i18n.language);
i18n.on("languageChanged", applyDir);

